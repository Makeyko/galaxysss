<h2>Существа с Андромеды (Андромедиане)</h2>

<p><span class="blu">Имя члена Галактической Федерации</span> – Андромедиане.</p>

<p><span class="blu">Нахождение в Галактической Федерации</span> - приблизительно 3.5 миллионов лет.</p>

<p><span class="blu">Местонахождение</span> - приблизительно от 150 до 4.000 Световых лет.</p>

<p><span class="blu">Форма жизни</span> - Гуманоиды</p>

<p><span
        class="blu">Физические характеристики</span> - Гуманоиды, похожие на жителей Земли. Они, обычно одеваются в традиционные многоцветные, выпрыгивающие костюмы. Андромедиане - включают 2 типа гуманоидных существ:
</p>

<p>Первый - "Кавказский", который варьируется от так называемого "Нордического" типа (блондины, голубые глаза, светлая кожа) до "Средиземноморского" (от светло до темно коричневых волос, от серых до коричневых глаз, кожу, которая выглядит загорелой). </p>

<p>Второй тип - типично "Восточный", с темными волосами, темными азиатскими глазами, и типом кожи, который варьируется от бледно - коричневого до темно - коричневого.</p>

<p>Глаза всех Андромедианцев немного больше, чем земных людей. Губы - тонкие и окрашены почти в светло - розовый цвет. Их уши расположены немного ниже на голове, и немного меньше в размере. Их руки и ноги выглядят нежно с длинными пальцами рук и ног.</p>

<p>Мужчины имеют обычно рост от 1.7- до 2.12 м и выше. Женщины имеют рост от 1.63 до 1.93 м и выше. Женщины андромедианки знают об их волшебных энергиях и об их миловидных фигурах.</p>

<p><span
        class="blu">Специальные качества и способности</span> - Андромедиане известны по всей Галактике, как мастера всех форм науки.
</p>

<p><span class="blu">Средняя необходимость во сне</span> - около 2 часов в день.</p>

<p><span
        class="blu">Язык</span> - Язык Андромедианцев варьируется от диалекта, похожего на итальяно - испанский язык, до более тонально - гортанного звука.
</p>

<p><span
        class="blu">Материнские корабли и другие летающие аппараты:</span> Андромедиансие корабли варьируются в виде от их традиционной самбрерообразной формы их разведывательных кораблей, которые имеют от 15 до 20 метров, до линзовидной формы их атмосферных командных кораблей, которые имеют длину 805 метров.
</p>

