<h2>Цэнтуриане (Кентавры)</h2>

<p><span class="blu"> Имя члена Галактической Федерации</span> - Великая звезда объединенного Кентавра.</p>

<p><span class="blu">Время вступления в Галактическую Федерацию</span> - 1.1 миллиона лет назад.</p>

<p><span
        class="blu">Месторасположение</span> - Созвездие Кентавра находится между созвездий Lupus (волчицы) и Vela (нёбо?). Известна, как ярчайшая звезда тройной звездной системы Альфа - Центавры (Известна так же, как Ригель Кентавру-с.) Одной из тройных звезд является Проксими Центури, которая наиболее близка к Земле - 4.3 световых лет.
</p>

<p><span class="blu">Расстояние до Земли</span> - от 4.3 световых лет до 1000 световых лет.</p>

<p><span class="blu">Жизненная форма</span> - Гуманоидный и рептоидный виды.</p>

<p><span
        class="blu">Физические данные</span> - Гуманоидные кентавры - напоминают людей, сейчас проживающих на Земле. Мужчины - очень мускулисты и хорошо сложены. Имеют светлые, коричневые, черные или красные волосы. Их глаза немного напоминают по виду азиатские, иногда они закруглены. Глаза имеют коричневый, черный, голубой, зеленый или земляной цвет. Мужчины имеют рост от 1.8 м. до 2.4 метра. Цвет их кожи варьируется от темно - коричневого до слегка загорелого белого. Женщины - также хорошо сложены, но менее мускулистые. Имеют рост от 1.65 м. до 2.1 м.
</p>

<p>Рептоидные кентавры имеют чешуйчатое (как у ящериц) и мускулистое тело. Кожа - пятнистая, имеет цвета: зеленый и голубой или красный и зеленый. Руки узкие, как у людей, имеют 6 пальцев, которые оканчиваются острым, кривым когтем. Глаза - круглые и выпуклые, выглядят как рептоидные, с вертикальным разрезом глаза имеют светло-красный или золотистый цвет. Они не имеют хвоста, но на ногах имеют пять длинных пальцев, заканчивающихся кривым когтем. Женщины имеют рост до 2.4 м. мужчины немного ниже.</p>

<p><span
        class="blu">Специальные способности и возможности</span> - Кентавры известны, как отличные стратеги, и часто являются Связными Консулами Галактической Федерации. Они отличились способностью объединения к единству групп различных Существ. Все это они достигали мирным и гармоничным путем.
</p>

<p><span class="blu">Средняя необходимость во сне</span> - 2 - 4 часа в сутки.</p>

<p><span
        class="blu">Язык</span> - гуманоидный язык немного гортанный, напоминает немецкий, но тональные звуки - похожи на китайский язык. Рептоидный язык очень гортанный и наполнен звуками, которые чрезвычайно сложны для воспроизведения человеком.
</p>

<p><span
        class="blu">Материнские корабли и другие летальные аппараты</span> - Кентавры имеют 2 типа летальных средств. Первый - многоцелевой разведывательный корабль. Он имеет вид колокола с большим круглым линзовидным крылом, прикрепленном на одной из сторон. Размер - приблизительно 14 метров в диаметре, и 9.1 метра в высоту. Второй тип - это командный корабль, который имеет вид сигары с выпуклостью по середине, приблизительно 60 метров в длину.
</p>

