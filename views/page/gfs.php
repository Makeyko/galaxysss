<p>Федерация приставляет сейчас, более 200.000 звездных наций, конфедераций или союзов. Примерно 40% из них - гуманоиды, остальные - различные формы чувствительных существ. Большинство членов Галактической Федерации - существа, обладающие полным сознанием.</p>

<p>Чтобы познакомить вас с нашей космической семьёй, мы будем периодически представлять информацию о типах сущностей, входящих в Галактическую Федерацию. Мы надеемся, что эта информация - поможет вам опознать, или признать какие - либо эксперименты, которые вы могли проводить с кем- нибудь из этих существ. Наша цель - в продвижении доброй воли и понимания звездных наций, которые отвечают на призыв Земной Духовной Иерархии о помощи в вашем процессе вознесения / трансформации.</p>

