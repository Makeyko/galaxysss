<h2>Арктурианцы</h2>


<p><span class="blu">Имя члена Федерации</span> - Арктурианская конфедерация.</p>

<p><span class="blu">Вхождение в Галактическую Федерацию</span> - Приблизительно 3.75 миллионов лет назад.</p>

<p><span class="blu">Месторасположение</span> - Звезда Арктурес в Созвездии "Сапога".</p>

<p><span class="blu">Расстояние от Земли</span> - Приблизительно 36 Световых лет.</p>

<p><span class="blu">Жизненная форма</span> - Лошадеобразные чувствительные существа.</p>

<p><span
        class="blu">Физические данные:</span> Арктурианцы - это очень чувствительные млекопитающие сущности, которые напоминают лошадь. Тело в определенной степени высокое и тонкое, с головой напоминающей лошадиную. Цвет варьируется в расцветке, от ванильного до очень темно-коричневого. Арктурианцы имеют гриву сзади шеи и головы и чрезвычайно тонкий хвост, который лишь чуть-чуть напоминает хвост лошади. Руки имеют 4 очень длинных пальца, гибких и очень тонких. Глаза арктурианцев намного больше в размере, чем людей с Земли, и они, или блекло-голубые или темно-коричневые. Уши немного похожи на лошадиные, но они меньше и более округленные, чем у их земных двойников. В размере, Арктурианцы мужчины имеют рост от 2.31 м до 2.64м. Женщины от 2.31 м до 2.49 м.
</p>

<p><span
        class="blu">Специальные таланты и возможности:</span> В Галактической Федерации Арктурианцы известны, как мастера времени (галактические календари и так далее), а также их особенными способностями в науке и философии.
</p>

<p><span class="blu">Средняя необходимость в сне: </span>Обычно от 1 до 3 часов в день.</p>

<p><span class="blu">Язык:</span> Говорят на тональном языке, чьи звуки немного похожие на китайский или вьетнамский
    <span class="blu">языки. </span></p>

<p><span
        class="blu"> Материнские корабли и другие летальные аппараты:</span> Арктурианские корабли различаются по форме, от водолазного колокола разведывательных кораблей, которые варьируются по размеру от 12.2 до 22.9 м в диаметре, до линзообразных планетарных командных кораблей, которые более 22.5 км.! в длину.
</p>

<h2>Беллатрианцы</h2>

<p><span
