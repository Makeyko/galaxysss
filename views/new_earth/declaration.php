            своей Цивилизации.</p>

        <p>2. Человечество имеет Цель — прогресс, созидание, развитие и освоение территории своей планеты Земля, а
            также освоение других территорий нашей галактики, на паритетных условиях взаимодействия с другими
            Разумными Цивилизациями и их сообществами.</p>

        <p>3. Человечество объединяет в себе Свободных людей которые только свободными, могут наиболее эффективно
            для Цивилизации, созидать, творить, развивать.</p>

        <p>4. Человечество имеет свои границы — по внешнему радиусу атмосферы планеты Земля.</p>

        <p>5. Человечество, в целях идентификации среди других цивилизаций, имеет свои аттрибуты: Символ, Герб,
            Знамя, Гимн.</p>

        <p>6. Человечество имеет одну единую религию, признающую Одного Единого Бога (Создателя) и объединяющую в
            себе все мировые религии и конфессии.</p>

        <p>7. Человечество имеет обязанности перед каждым живущим человеком. От поколения к поколению, оно создает
            для него постоянно улучшающиеся условия его личной жизни.</p>

        <p>8. Человечество имеет обязанности перед потомками в целом. Оно, своими жизнями и трудом создает более
            лучшие условия жизни для будущих поколений Людей.</p>

        <p>9. Будущие поколения Человечества имеют обязанности перед прошлыми поколениями Человечества. В будущем,
            они обязаны достичь таких технических условий контроля и перехода во времени, при которых восстановлена
            и воссоздана поименная информация обо всех людях, живущих на планете Земля от начала Времен, а также о
            жизненном пути каждого из них. Они должны найти способ сохранения сознания и разума каждого человека
            после смерти его физического тела. Их задача, в качестве благодарности (долга) перед предыдущими
            поколениями, реализовать принцип идущий из прошлого в будущее; «Мы создали по крупицам эту Цивилизацию —
            для Вас, создайте жизнь после смерти - для Нас».</p>

        <h2 class="text-center">ЧЕЛОВЕК</h2>

        <p>10. Исходя из пункта 9 настоящей декларации каждый человек живший когда-либо на планете земля, имеет свое
            основное право — право на продолжение разумной жизни после смерти физического тела. Таким образом,
            каждый человек должен осознавать что его жизнь на Земле имеет смысл, отслеживается, сопровождается и
            будет продолжена после смерти. Ей будет дана оценка будущими справедливыми поколениями Людей.</p>

        <p>11. человек имеет право:</p>

        <p>- Жить, иметь потомство и осуществлять деятельность в интересах Человечества, на планете Земля;</p>

        <p>- Свободно перемещаться по всей территории планеты Земля;</p>

        <p>- Иметь неограниченный доступ и пользоваться всей информацией, которая формировалась, формируется и будет
            формироваться в будущем на планете Земля;</p>

        <p>- Использовать все природные ресурсы находящиеся внутри, под водой и на поверхности планеты Земля;</p>

        <p>- Использовать все научные и технические достижения, открытия и исследования, осуществленные и
            произведенные на планете Земля.</p>

        <p>- В равной мере, иметь доступ и использовать энергию производимую Человечеством.</p>

        <p>12. Человек имеет обязанности перед семьей, перед Человечеством, перед последующими поколениями — своими
            потомками.</p>

        <h2 class="text-center"> СТРОЕНИЕ</h2>

        <p>13. Человечество имеет строение: Человек — Семья — Человечество.</p>

        <p>14. Все государственные системы управления людьми (государства), существовавшие до принятия Декларации,
