        <p>- Осуществляет управление всеми аспектами жизнедеятельности Человечества</p>

        <p>- Осуществляет скоординированную общепланетарную экономическую и социальную политику;</p>

        <p>- Осуществляет представительские функции, от Имени Человеческой Цивилизации, при взаимодействии с другими
            Цивилизациями.</p>

        <h2 class="text-center">СТАТУС ЧЕЛОВЕЧЕСТВА И ВНЕШНЕЕ ВЗАИМОДЕЙСТВИЕ</h2>

        <p>17. Человечество продолжая свой эволюционный развития на принципах самоопределения принимает настоящую
            декларацию, на основании чего получает статус «Разумной Цивилизации», который дает права и
            обязанности.</p>

        <p>18. Человечество как Цивилизация получает право вступать во взаимодействие с другими Разумными
            Цивилизациями.</p>

        <p>19. Человечество как Цивилизация получает право входить в сообщество и Объединения Инопланетных
            Цивилизаций, на равных правах и обязанностях.</p>

        <p> 20. Человечество приняв статус «Разумной Цивилизации», получает право на суверенитет и заявляет о
